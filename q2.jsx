// Working Directory: This is where you make changes to your files. It's your local sandbox where you modify, add, or delete files.

// Staging Area (Index): After making changes in the working directory, you stage them in the index. Staging is like preparing a snapshot of the changes you want to commit. This allows you to selectively include or exclude changes before committing them.

// Repository: The repository is where Git permanently stores the committed changes. It includes a complete history of all changes, branches, and tags.

// The basic Git workflow involves:

// Modify files in the working directory.
// Stage the changes using git add to move them to the staging area.
// Commit the changes using git commit to save them in the repository.